puts "What's your fav color?"
fav_color=gets.chomp
case fav_color
when "red" then
	puts "Red like fire!"
when "orange"
	puts "Orange like...an orange."
when "yellow"
	puts "Daffodils so pretty"
when "green"
	puts "You been to emerald city?"
when "blue"
	puts "BLue like the sky."
when "purple"
	puts "yum, plums."
else
	puts "I don't know that color."
end